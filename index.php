                <h2 class="page-section-heading text-center text-uppercase text-white bg-dark" mb-0>Aplicaciones Web hibridas orientadas a servicios.</h2>
  
  
    <br>


Las aplicaciones híbridas son aplicaciones móviles diseñadas en un lenguaje de programación web ya sea HTML5, CSS o JavaScript, junto con un framework que permite adaptar la vista web a cualquier vista de un dispositivo móvil. En otras palabras, no son más que una aplicación construida para ser utilizada o implementada en distintos sistemas operativos móviles, tales como, iOS, Android o Windows Phone, evitándonos la tarea de crear una aplicación para cada sistema operativo. De esta manera, una aplicación híbrida puede ser adaptada a múltiples plataformas móviles sin crear nuevos códigos, pero ajustándose a algunos cambios operacionales para cada uno de ellos.      <br>
   <br><center>
<img src="img/img4.jpg"style="width:700px; height: 300px;">
           <br></center>
  <br>
Sin embargo, a pesar que el desarrollo de aplicaciones híbridas y nativas requiere de una construcción totalmente distinta, la forma de utilizarlas es igual. Para ello, solo debes dirigirte hasta la tienda de aplicaciones de tu dispositivo móvil, buscar la App que quieres instalar y descargarla. Aunque ambas son iguales en su forma de usabilidad, el rendimiento de una aplicación híbrida comparada con una nativa es mucho menor, debido a que estas últimas aprovechan de forma más óptima los recursos de hardware del dispositivo, por ejemplo, la cámara, el GPS, los sensores en el interior del dispositivo, entre otros. Por el contrario, las aplicaciones híbridas también pueden utilizar estos recursos de hardware, pero no al mismo nivel en comparación con las nativas. 

